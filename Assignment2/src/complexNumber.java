public class complexNumber {
private int m, n;

public complexNumber(){

}

public complexNumber(int x ,int y){
    this.m = x;
    this.n = y;
}

    public int getM() {
        return m;
    }

    public int getN() {
        return n;
    }

    public void setM(int m) {
        this.m = m;
    }

    public void setN(int n) {
        this.n = n;
    }
}

